//Assignment operator
class variable{
    public static void main(String args[]){
        int a=10;
        int var;
        var=a;
        System.out.println("using var:="+var);
        var +=a;
        System.out.println("using var:="+var);
        var *=a;
        System.out.println("using var:="+var);
        var /=a;
        System.out.println("using var:="+var);
        
    }
}